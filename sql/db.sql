CREATE TABLE IF NOT EXISTS restaurantes(
    restauranteId integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    description text,
    logo text,
    rating decimal,
    name text NOT NULL CHECK (name <> '')
);

CREATE TABLE IF NOT EXISTS categorias(
    categoriaId INTEGER PRIMARY KEY GENERATED BY DEFAULT  AS IDENTITY,
    name text NOT NULL CHECK (name <> '')
);

CREATE TABLE IF NOT EXISTS resenias(
    reseniaId INTEGER PRIMARY KEY GENERATED BY DEFAULT  AS IDENTITY,
    comments text,
    rating decimal,
    email text NOT NULL CHECK (email <> '')
);

CREATE TABLE IF NOT EXISTS restaurantecategorias(
    restaurantecategoria integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    restauranteId INTEGER REFERENCES restaurantes(restauranteId),
    categoriaId INTEGER REFERENCES categorias(categoriaId)
);

CREATE TABLE IF NOT EXISTS restauranteresenias(
    restauranteresenia integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    restauranteId INTEGER REFERENCES restaurantes(restauranteId),
    reseniaId INTEGER REFERENCES resenias(reseniaId)
);

