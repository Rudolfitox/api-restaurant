CREATE TABLE IF NOT EXISTS restaurants(
    slug integer PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    description text,
    logo text,
    rating decimal,
    name text NOT NULL CHECK (name <> '')
);

CREATE TABLE IF NOT EXISTS categories(
    slug INTEGER PRIMARY KEY GENERATED BY DEFAULT  AS IDENTITY,
    name text NOT NULL CHECK (name <> '')
);

CREATE TABLE IF NOT EXISTS reviews(
    slug INTEGER PRIMARY KEY GENERATED BY DEFAULT  AS IDENTITY,
    comments text,
    rating decimal,
    email text NOT NULL CHECK (email <> '')
);

CREATE TABLE IF NOT EXISTS restaurant_category(
    restaurantId INTEGER REFERENCES restaurants(slug),
    categoryId INTEGER REFERENCES categories (slug)
);

CREATE TABLE IF NOT EXISTS restaurant_review(
    restaurantId INTEGER REFERENCES restaurants(slug),
    reviewId INTEGER REFERENCES reviews(slug)
);

